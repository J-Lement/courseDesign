package bean;

public class SubjectBean {
    private int subId;
    private int schId;
    private int majId;
    private String subName;

    public int getSubId() {
        return subId;
    }

    public void setSubId(int subId) {
        this.subId = subId;
    }

    public int getSchId() {
        return schId;
    }

    public void setSchId(int schId) {
        this.schId = schId;
    }

    public int getMajId() {
        return majId;
    }

    public void setMajId(int majId) {
        this.majId = majId;
    }

    public String getSubName() {
        return subName;
    }

    public void setSubName(String subName) {
        this.subName = subName;
    }
}
