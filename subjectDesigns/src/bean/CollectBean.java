package bean;

public class CollectBean {
    private int userId;
    private int bookId;
    private int ifCol;

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getBookId() {
        return bookId;
    }

    public void setBookId(int bookId) {
        this.bookId = bookId;
    }

    public int getIfCol() {
        return ifCol;
    }

    public void setIfCol(int ifCol) {
        this.ifCol = ifCol;
    }
}
